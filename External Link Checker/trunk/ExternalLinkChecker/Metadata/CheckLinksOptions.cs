namespace ExternalLinksChecker.Metadata
{
  using System.Collections.Generic;

  using Sitecore.Data.Items;
  using Sitecore.Globalization;

  /// <summary>
  /// The links options.
  /// </summary>
  public class LinksOptions
  {
    #region Properties

    /// <summary>
    /// Gets or sets the languages.
    /// </summary>
    public List<Language> Languages { get; set; }

    /// <summary>
    /// Gets or sets the root item.
    /// </summary>
    public Item Root { get; set; }

    #endregion

    #region Constructors

    /// <summary>
    /// Initializes a new instance of the <see cref="LinksOptions"/> class.
    /// </summary>
    /// <param name="root">
    /// The root.
    /// </param>
    public LinksOptions(Item root)
    {
      this.Root = root;
    }

    #endregion
  }
}